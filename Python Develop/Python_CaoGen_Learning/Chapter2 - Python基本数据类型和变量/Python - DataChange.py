'''
python的基本数据转换，数据转换的方法如下：
1)、 int(x [,base ])         将x转换成一个整数
2)、 long(x [,base ])        将x转换成一个长整数
3)、 float(x)                将x转换成一个浮点数
4)、 complex(real [,imag])   创建一个复数
5)、 str(x)                  将x转换成一个字符串
6)、 repr(x)                 将x转换为表达式字符串
7)、 eval(str)               计算字符串中的有效python表达式，并返回一个对象
8)、 tuple(s)                将序列s转换为一个元组
9)、 list(s)                 将序列s转换为一个list列表
10)、chr(x)                  将一个整数转换为一个字符
11)、unichr(x)               将一个整数转换为Unicode字符
12)、ord(x)                  将一个字符转换为对应的整数值
13)、hex(x)                  将一个整数转换为一个十六进制字符串
14)、oct(x)                  将一个整数转换为一个八进制字符串
'''